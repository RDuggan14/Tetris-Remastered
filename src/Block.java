public class Block {
    private int xcord;
    private int ycord;
    //Constructor to create the block
    public Block(int xcord, int ycord){
        this.xcord = xcord;
        this.ycord = ycord;
    }

    public int xcord(){
        return(this.xcord);
    }
    public int ycord(){
        return(this.ycord);
    }


}
